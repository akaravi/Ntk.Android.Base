package ntk.android.base.appclass;

public class FromToClass {
    Object from;
    Object to;

    public void setFrom(Object f) {
        from = f;
    }

    public void setTo(Object f) {
        to = f;
    }

    public Object getFrom() {
        return from;
    }

    public Object getTo() {
        return to;
    }
}
